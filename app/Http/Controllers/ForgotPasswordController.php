     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // return 'ok';
        $request->validate(['email' => 'required|email']);



        try {
            $status = Password::sendResetLink(
                $request->only('email')
            );
        } catch (\Exception $e) {
            return response()->json(['status'=>0,'notification'=>"Check network connection"]);
        }

        if ($status === Password::RESET_LINK_SENT) {
            return response()->json(['status'=>1,'notification'=>"Link as been sent to your email"]);
        } else {
            return response()->json(['status'=>0,'notification'=>"Try again later"]);
        }
